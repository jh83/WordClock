void showChristmas()
{
  int currMonth = month();
  int currDay = day();

  if (currMonth == 12 && currDay == 24)
    showScreen(godJul, settings.dimLevel);
}

void updateDisplay()
{
  Serial.println("Updating Display");
  clearScreen(); // Clear the pixels
  showClock(); // Add the Clock
  showBirthday();
  showChristmas();

  leds.show(pixels); // Send the pixels to the display
}

